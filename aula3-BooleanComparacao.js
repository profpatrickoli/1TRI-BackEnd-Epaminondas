// OPERADORES DE COMPARAÇÃO
// Operador de igualdade (compara apenas o valor)
console.log("1" == 1)
console.log(true == 1)
console.log(false == 0)
console.log(false == "")

// Operador de igualdade estrita 
// (compara valor e tipo)
console.log("1" === 1)
console.log(true === 1)
console.log(false === 0)

// Verificar tipo da variável
console.log(typeof("1")) // -> texto (string)
console.log(typeof(1)) // -> Number
console.log(typeof(true)) // -> boolean